
def greet(name, age):
    print(f"Hello, my name is {name} and I am {age} years old")

greet("Manusha", 23)
greet("Nirmal", 24)
greet("Perera", 25)


def add(num1, num2):
    return num1 + num2

def substract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

print(f"3 + 2 = {add(3,2)}")
print(f"3 - 2 = {substract(3,2)}")
print(f"3 * 2 = {multiply(3,2)}")
print(f"3 / 2 = {divide(3,2)}")