name="Nirmal Perera"
age=23
gpa=3.56
is_student=True

print(type(name)) # <class 'str'>
print(type(age)) # <class 'int'>
print(type(gpa)) # <class 'float'>
print(type(is_student)) # <class 'bool'>

# type casting -> str(), int(), float(), bool()
print(float(age)) # 23.0
print(str(age)) # 23
print(int(gpa)) # 3
print(bool(age)) # True